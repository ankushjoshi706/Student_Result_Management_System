    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String course = (String)jComboBox1.getSelectedItem();
        String branch = (String) jComboBox2.getSelectedItem();
        String rollno=jTextField1.getText();
        String name=jTextField2.getText();
        String gender = (String) jComboBox3.getSelectedItem();
        String fathername=jTextField3.getText();
        String mobileno = jTextField4.getText();
        //show some exception box
        try{
            //connection form the databse
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con= DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "Puchu@2001");
            
            Statement st = con.createStatement();
            st.executeUpdate("insert into student(rollno,course,branch,name,gender,fathername,mobileno) values('"+rollno+"', '"+course+"', '"+branch+"', '"+name+"', '"+gender+"', '"+fathername+"', '"+mobileno+"')");
            // show message when data in inserted
            JOptionPane.showMessageDialog(null, "Data updated successfully");
            //after adding new student data a new window will open for entering again student data
            setVisible(false);
            new index().setVisible(true);
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

