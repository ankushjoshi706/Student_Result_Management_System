    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String course = (String)jComboBox1.getSelectedItem();
        String branch = (String)jComboBox2.getSelectedItem();
        if(course=="B.tech"&& branch=="EEE"){
            setVisible(false);
            new btechee().setVisible(true);
        }
        if(course=="B.tech"&& branch=="CSE"){
            setVisible(false);
            new btechcse().setVisible(true);
        }
        if(course=="B.tech"&& branch=="IT"){
            setVisible(false);
            new btechit().setVisible(true);
        }
        if(course=="B.tech"&& branch=="ECE"){
            setVisible(false);
            new btechece().setVisible(true);
        }
        if(course=="B.tech"&& branch=="ME"){
            setVisible(false);
            new btechme().setVisible(true);
        }
        if(course=="M.tech"){
            setVisible(false);
            new mtech().setVisible(true);
        }
        if(course=="BBA"){
            setVisible(false);
            new bba().setVisible(true);
        }
        if(course=="MBA"){
            setVisible(false);
            new mba().setVisible(true);
        }
        if(course=="BCA"){
            setVisible(false);
            new bca().setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

