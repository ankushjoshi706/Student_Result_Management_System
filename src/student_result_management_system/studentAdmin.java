    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String course = (String)jComboBox1.getSelectedItem();
        String branch = (String)jComboBox2.getSelectedItem();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con= DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "Puchu@2001");
            
            Statement st=con.createStatement();
            //checking if roll no is present or not
             ResultSet rs=st.executeQuery("select * from student inner join resultcse inner join resulteee inner join resultbba  inner join resultbca inner join resultit inner join resultece inner join resultmba inner join resultme inner join resultmtech where student.rollno='"+rollno+"' and resultcse.rollno='"+rollno+"' or student.rollno='"+rollno+"' and resulteee.rollno='"+rollno+"' or student.rollno='"+rollno+"' and resultbba.rollno='"+rollno+"' or student.rollno='"+rollno+"' and resultbca.rollno='"+rollno+"' or student.rollno='"+rollno+"' and resultit.rollno='"+rollno+"' or student.rollno='"+rollno+"' and resultece.rollno='"+rollno+"' or student.rollno='"+rollno+"' and resultmba.rollno='"+rollno+"' or student.rollno='"+rollno+"' and resultme.rollno='"+rollno+"' or student.rollno='"+rollno+"' and resultmtech.rollno='"+rollno+"'");
             if(rs.next()){
                 //setVisible(false);
                 //new studentHome(rollno).setVisible(true);
                 if(course=="B.tech"&& branch=="EEE"){
                       setVisible(false);
                       new studentresulteee(rollno).setVisible(true);
                    }
                 
                 else if(course=="B.tech"&& branch=="CSE"){
                      setVisible(false);
                      new studentresultcse(rollno).setVisible(true);
                    }
                  
                 else if(course=="B.tech"&& branch=="IT"){
                       setVisible(false);
                       new studentresultit(rollno).setVisible(true);
                    }
                  
                 else if(course=="B.tech"&& branch=="ECE"){
                        setVisible(false);
                        new studentresultece(rollno).setVisible(true);
                    }
                  
                 else if(course=="B.tech"&& branch=="ME"){
                       setVisible(false);
                       new studentresultme(rollno).setVisible(true);
                    }
                  
                 else if(course=="M.tech" && branch=="Other"){
                      setVisible(false);
                      new studentresultmtech(rollno).setVisible(true);
                    }
                  
                 else if(course=="BBA" && branch=="Other"){
                      setVisible(false);
                      new studentresultbba(rollno).setVisible(true);
                    }
                  
                 else if(course=="MBA" && branch=="Other"){
                      setVisible(false);
                      new studentresultmba(rollno).setVisible(true);
                    }
                  
                 else if(course=="BCA" && branch=="Other"){
                      setVisible(false);
                      new studentresultbca(rollno).setVisible(true);
                    }
                  else{
                     JOptionPane.showMessageDialog(null, "Course and Branch are incorrect");
                 }
                  
                  
             }
             else{
                 JOptionPane.showMessageDialog(null, "Incorrect roll number");
             }
             
             
             
             
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

